    private void caixaInsCEPFornKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_caixaInsCEPFornKeyReleased
        // TODO add your handling code here:
        ViaCepAPI vcAPI = new ViaCepAPI();
        Boolean valCep = new ValidacaoRegex().validarCep(caixaInsCEPForn.getText());

        if (valCep) {
            
            Endereco endereco;
            try {
                endereco = vcAPI.getEndereco(caixaInsCEPForn.getText());
                
                caixaInsBairroForn.setText(endereco.getBairro());
                caixaInsCidForn.setText(endereco.getLocalidade());
                caixaInsCompForn.setText(endereco.getComplemento());
                caixaInsEstForn.setText(endereco.getUf());
                caixaInsRuaForn.setText(endereco.getLogradouro());
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Falha em obter dados do CEP: " + ex);
            } catch (org.apache.hc.core5.http.ParseException ex) {
                Logger.getLogger(JanelaFornecedor.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } else {
            caixaInsBairroForn.setText("");
            caixaInsCidForn.setText("");
            caixaInsCompForn.setText("");
            caixaInsEstForn.setText("");
            caixaInsRuaForn.setText("");
        }
    }//GEN-LAST:event_caixaInsCEPFornKeyReleased

