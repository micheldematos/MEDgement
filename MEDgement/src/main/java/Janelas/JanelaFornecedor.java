    private void botaoInativarFornActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoInativarFornActionPerformed
        // TODO add your handling code here:
        Fornecedor f = model.pegaDadosLinha(tabelaFornecedores.getSelectedRow());
        
        if ("Inativar".equals(botaoInativarForn.getText())) {
            int inativarForn = JOptionPane.showConfirmDialog(null, "Inativar fornecedor?", "CONFIRMAR", JOptionPane.YES_NO_OPTION);
            
            if (inativarForn == 0) {
                FornecedorDAO dao = new FornecedorDAO();
                dao.inativar(f);
                limpaCampos();
                ocultar();
            } else if (inativarForn == 1){
                ocultar();
            }
        } else {
            int ativarForn = JOptionPane.showConfirmDialog(null, "Ativar fornecedor?","CONFIRMAR",JOptionPane.YES_NO_OPTION);
            if (ativarForn == 0) {
                FornecedorDAO dao = new FornecedorDAO();
                dao.ativar(f);
                limpaCampos();
                ocultar();
            } else if (ativarForn == 1){
                ocultar();
            }   
        }
    }//GEN-LAST:event_botaoInativarFornActionPerformed

    private void tabelaFornecedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaFornecedoresMouseClicked
        // TODO add your handling code here:
        if (tabelaFornecedores.getSelectedRow() != -1 && tabelaFornecedores.getSelectedRow() != clique) {
            clique = tabelaFornecedores.getSelectedRow();

            botaoInativarForn.setVisible(true);

            Fornecedor f = model.pegaDadosLinha(tabelaFornecedores.getSelectedRow());
            caixaInsIEForn.setText(f.getInscricaoestadualfornecedor());
            caixaInsCNPJForn.setText(f.getCnpjfornecedor());
            caixaInsNomeForn.setText(f.getNomefornecedor());
            caixaInsRuaForn.setText(f.getRuafornecedor());
            caixaInsNumForn.setText(f.getNumerofornecedor());
            caixaInsCompForn.setText(f.getComplementofornecedor());
            caixaInsBairroForn.setText(f.getBairrofornecedor());
            caixaInsCidForn.setText(f.getCidadefornecedor());
            caixaInsEstForn.setText(f.getEstadofornecedor());
            caixaInsCEPForn.setText(f.getCepfornecedor());

            URL ativar = getClass().getResource("/Logo/Ativar.png");
            Icon activate = new ImageIcon(ativar);
            URL inativar = getClass().getResource("/Logo/Inativar.png");
            Icon desactivate = new ImageIcon(inativar);

            if (f.getSituacaofornecedor() == 0) {
                botaoInativarForn.setText("Ativar");
                botaoInativarForn.setIcon(activate);
            } else {
                botaoInativarForn.setText("Inativar");
                botaoInativarForn.setIcon(desactivate);
            }

            consultaFornecedor(f.getCodfornecedor(), f.getRuafornecedor(), f.getNumerofornecedor(),
                    f.getBairrofornecedor(), f.getCidadefornecedor(), f.getEstadofornecedor(),
                    f.getCepfornecedor(), f.getComplementofornecedor());
        } else {
            ocultar();
        }
    }//GEN-LAST:event_tabelaFornecedoresMouseClicked

    public void consultaFornecedor(int cod, String rua, String num, String bairro, 
            String cidade, String estado, String CEP, String complemento){
        consultaItens.setVisible(true);
        
        consultaRua.setText(rua);
        consultaNum.setText(num);
        consultaBairro.setText(bairro);
        consultaCidade.setText(cidade);
        consultaEstado.setText(estado);
        consultaCEP.setText(CEP);   
        consultaCod.setText(String.valueOf(cod));
        
        if (complemento.isEmpty()) {
            consultaComplemento.setText("Sem complemento");
        } else {
            consultaComplemento.setText(complemento);
        }
    }
    
